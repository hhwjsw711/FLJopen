export function getLang(): Lang {
  if (typeof window === 'undefined') return 'zh'
  const saved = localStorage.getItem('flj_lang') as Lang | null
  if (saved && saved in T) return saved
  // 自动检测浏览器语言
  const bl = navigator.language.toLowerCase()
  if (bl.startsWith('ja')) return 'ja'
  if (bl.startsWith('zh-tw') || bl.startsWith('zh-hant')) return 'zh-tw'
  if (bl.startsWith('zh')) return 'zh'
  if (bl.startsWith('en')) return 'en'
  return 'zh'
}

export function setLangStorage(lang: Lang) {
  if (typeof window !== 'undefined') localStorage.setItem('flj_lang', lang)
}
